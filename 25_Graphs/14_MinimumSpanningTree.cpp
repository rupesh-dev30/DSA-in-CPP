/*
    Minimum Spanning Tree (MST)
    ===========================
    
    Definition: A Minimum Spanning Tree of a connected, edge-weighted undirected graph is a subset of edges that connects all vertices with the minimum possible total edge weight and without any cycles.
  
    Applications: Used in network design, such as designing minimum-cost communication networks, road connections, and electrical grids.

    Key Properties:
      
      No Cycles: MST does not contain cycles.

      Minimum Weight: Sum of the edge weights in MST is minimal.

      Spanning: Includes all vertices of the original graph.

      MST Algorithms in C++

      Two popular algorithms for finding MST are 
      1. Prim's Algorithm
      2. Kruskal's Algorithm.
*/