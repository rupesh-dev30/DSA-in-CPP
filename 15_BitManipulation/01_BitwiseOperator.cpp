#include <bits/stdc++.h>
using namespace std;

int main() {
  // code here
  cout << (3 & 5) << endl;
  cout << (3 | 5) << endl;
  cout << (3 ^ 5) << endl;

  return 0;
}

/*
    Bitwise Operators:

      1. AND -> &   : It give 0 when atleast 1 is zero
      2. OR -> |    : It give 0, when both are 0
      3. XOR -> ^   : It give 0 when same
*/